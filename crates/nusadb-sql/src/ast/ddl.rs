/// One declared parameter of a stored procedure.
///
/// An `IN` parameter (the default) is referenced in the body positionally as `$1`..`$n` (in `IN`-only
/// order). An `OUT` parameter is a named output: the body `SET`s a variable of that name, and `CALL`
/// returns the final values of all `OUT` parameters as a result row. The declared type is accepted
/// but not yet used to coerce the value (a follow-up).
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ProcedureParam {
    /// Parameter name (the `OUT` parameter is read back by this name).
    pub name: String,
    /// Declared parameter type.
    pub ty: ColumnType,
    /// `true` for an `OUT` parameter (a named output), `false` for an ordinary `IN` parameter.
    pub out: bool,
}

