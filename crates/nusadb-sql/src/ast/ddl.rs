/// A single `ALTER TABLE` action.
///
/// Column actions (`ADD [COLUMN]`, `DROP [COLUMN]`, `RENAME COLUMN`,
/// `ALTER COLUMN`) and constraint actions (`ADD [CONSTRAINT]
/// <table_constraint>`, `DROP CONSTRAINT`) are modelled. Column positioning
/// (`FIRST`/`AFTER`) and `... CASCADE` are rejected by the parser.
#[derive(Debug, Clone, PartialEq)]
pub enum AlterTableAction {
    /// `ADD [COLUMN] [IF NOT EXISTS] <column_def>`.
    AddColumn {
        /// The column being added.
        column: ColumnDef,
        /// Whether `IF NOT EXISTS` was specified.
        if_not_exists: bool,
    },
    /// `DROP [COLUMN] [IF EXISTS] name` (implicit `RESTRICT`).
    DropColumn {
        /// Name of the column to drop.
        name: String,
        /// Whether `IF EXISTS` was specified.
        if_exists: bool,
    },
    /// `RENAME COLUMN from TO to`.
    RenameColumn {
        /// Current column name.
        from: String,
        /// New column name.
        to: String,
    },
    /// `RENAME TO name` — rename the table itself.
    RenameTable {
        /// The new table name.
        name: String,
    },
    /// `ADD [CONSTRAINT name] <table_constraint>`.
    AddConstraint(TableConstraint),
    /// `DROP CONSTRAINT [IF EXISTS] name` (implicit `RESTRICT`).
    DropConstraint {
        /// Name of the constraint to drop.
        name: String,
        /// Whether `IF EXISTS` was specified.
        if_exists: bool,
    },
    /// `ALTER [COLUMN] name <change>`.
    AlterColumn {
        /// Name of the column being altered.
        column: String,
        /// The modification to apply.
        change: ColumnChange,
    },
    /// `ENABLE ROW LEVEL SECURITY` — turn on row-level security for the table.
    EnableRowLevelSecurity,
    /// `DISABLE ROW LEVEL SECURITY` — turn off row-level security for the table.
    DisableRowLevelSecurity,
    /// `ENABLE TRIGGER {name | ALL}` — re-enable a disabled trigger (`None` = every trigger on
    /// the table).
    EnableTrigger {
        /// The trigger to enable, or `None` for `ALL`.
        name: Option<String>,
    },
    /// `DISABLE TRIGGER {name | ALL}` — stop a trigger from firing without dropping it (`None` =
    /// every trigger on the table).
    DisableTrigger {
        /// The trigger to disable, or `None` for `ALL`.
        name: Option<String>,
    },
}

/// A modification applied to an existing column by `ALTER [COLUMN]`.
///
/// `SET DATA TYPE ... USING <expr>` (a conversion expression) and
/// `ADD GENERATED ... AS IDENTITY` are out of scope for the v1 parser surface
/// and rejected with [`Error::Unsupported`](crate::error::Error::Unsupported).
#[derive(Debug, Clone, PartialEq)]
pub enum ColumnChange {
    /// `[SET DATA] TYPE <type>`.
    SetType(ColumnType),
    /// `SET DEFAULT <expr>` — carries the default expression and its canonical SQL text,
    /// like a column's `default_sql`, so the executor can persist + re-parse it.
    SetDefault {
        /// The default value expression (type-checked at analysis time).
        expr: Expr,
        /// The default's canonical SQL text, persisted in the column-default catalog.
        sql: String,
    },
    /// `DROP DEFAULT`.
    DropDefault,
    /// `SET NOT NULL`.
    SetNotNull,
    /// `DROP NOT NULL`.
    DropNotNull,
}

/// A table-level constraint, as carried by `ADD CONSTRAINT` (and, in future,
/// the table-constraint clause of `CREATE TABLE`).
///
/// Only the four standard constraint kinds are modelled. Index hints
/// (`USING <method>`, `KEY`/`INDEX` display, index options) and
/// deferrable-constraint characteristics are out of scope and rejected by the
/// parser with [`Error::Unsupported`](crate::error::Error::Unsupported).
#[derive(Debug, Clone, PartialEq)]
pub enum TableConstraint {
    /// `[CONSTRAINT name] PRIMARY KEY (columns)`.
    PrimaryKey {
        /// Optional constraint name.
        name: Option<String>,
        /// Key columns in declaration order; always non-empty.
        columns: Vec<String>,
    },
    /// `[CONSTRAINT name] UNIQUE (columns)`.
    Unique {
        /// Optional constraint name.
        name: Option<String>,
        /// Unique columns in declaration order; always non-empty.
        columns: Vec<String>,
    },
    /// `[CONSTRAINT name] FOREIGN KEY (columns) REFERENCES table (referred)
    /// [ON DELETE action] [ON UPDATE action]`.
    ForeignKey {
        /// Optional constraint name.
        name: Option<String>,
        /// Referencing columns in this table.
        columns: Vec<String>,
        /// Referenced (parent) table.
        foreign_table: String,
        /// Referenced columns in the parent table; empty means the parent's
        /// primary key.
        referred_columns: Vec<String>,
        /// `ON DELETE` action, if specified.
        on_delete: Option<ReferentialAction>,
        /// `ON UPDATE` action, if specified.
        on_update: Option<ReferentialAction>,
    },
    /// `[CONSTRAINT name] CHECK (expr)`.
    Check {
        /// Optional constraint name.
        name: Option<String>,
        /// The boolean predicate.
        expr: Expr,
        /// The predicate's canonical SQL text — persisted in the constraint catalog so
        /// the executor can re-parse and enforce it per row on later `INSERT`/`UPDATE`.
        predicate_sql: String,
    },
}

/// A referential action for a foreign key's `ON DELETE` / `ON UPDATE` clause.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ReferentialAction {
    /// `NO ACTION` — reject if dependent rows remain (default).
    NoAction,
    /// `RESTRICT` — reject immediately.
    Restrict,
    /// `CASCADE` — propagate the delete/update to dependent rows.
    Cascade,
    /// `SET NULL` — null out the referencing columns.
    SetNull,
    /// `SET DEFAULT` — reset the referencing columns to their default.
    SetDefault,
}

/// `CREATE [UNIQUE] INDEX [IF NOT EXISTS] name ON table (keys) [INCLUDE (cols)] [WHERE pred]`.
///
/// Supports plain-column keys, **functional/expression** keys (`(lower(s))`, `((a + b))`, in
/// `key_exprs`), and a **partial-index** `WHERE` predicate. Per-column `ASC`/`DESC` /
/// `NULLS FIRST`/`NULLS LAST` are rejected — only ascending indexes are built and ordered index
/// scans are not implemented, so accepting a direction would be a silent-lossy trap. `WITH (...)`,
/// `NULLS DISTINCT`, `CONCURRENTLY`, and operator classes are also rejected. `USING <method>` is
/// accepted only for the `hnsw` vector access method. An anonymous index (no name) is rejected —
/// names are required so the downstream catalog has a stable handle.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CreateIndex {
    /// Index name (required).
    pub name: String,
    /// Explicit schema qualifier of the target table: `Some(schema)` for `CREATE INDEX … ON
    /// schema.t`, `None` when unqualified (resolved through the search path).
    pub table_schema: Option<String>,
    /// Target table the index covers.
    pub table: String,
    /// Plain key column names in declaration order — for a plain-column index. Empty when the
    /// index keys on expressions (`key_exprs` is then non-empty). Exactly one of `columns` /
    /// `key_exprs` is non-empty.
    pub columns: Vec<String>,
    /// Key expressions in declaration order, as SQL text — for a functional/expression index.
    /// Empty for a plain-column index. Each is re-parsed and evaluated per row to build the key.
    pub key_exprs: Vec<String>,
    /// Partial-index `WHERE` predicate as SQL text; `None` for a full index. Only rows for which it
    /// is true are indexed.
    pub predicate: Option<String>,
    /// Optional non-key columns stored alongside the key (`INCLUDE (...)`),
    /// preserving declaration order. Empty when no `INCLUDE` clause was given.
    pub include: Vec<String>,
    /// `USING <method>` access method, folded to lowercase; `None` for the default
    /// (B-tree). Only `Some("hnsw")` is otherwise accepted — a vector index.
    pub using: Option<String>,
    /// Whether `UNIQUE` was specified.
    pub unique: bool,
    /// Whether `IF NOT EXISTS` was specified.
    pub if_not_exists: bool,
}
