/// The command a row-level-security policy applies to. `ALL` covers every command.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PolicyCommand {
    /// `FOR ALL` — the policy applies to every command (the default when `FOR` is omitted).
    All,
    /// `FOR SELECT`.
    Select,
    /// `FOR INSERT`.
    Insert,
    /// `FOR UPDATE`.
    Update,
    /// `FOR DELETE`.
    Delete,
}

impl PolicyCommand {
    /// The canonical keyword, used for catalog persistence and diagnostics.
    #[must_use]
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::All => "ALL",
            Self::Select => "SELECT",
            Self::Insert => "INSERT",
            Self::Update => "UPDATE",
            Self::Delete => "DELETE",
        }
    }
}

/// `CREATE POLICY name ON table [AS { PERMISSIVE | RESTRICTIVE }] [FOR command] [TO role[, ...]]
/// [USING (expr)] [WITH CHECK (expr)]` — a row-level-security policy.
///
/// `USING`/`WITH CHECK` expressions are captured as canonical SQL text and re-analyzed against the
/// target table. A permissive policy *grants* access (the applicable permissive policies are
/// `OR`-combined); a restrictive policy *narrows* it (the applicable restrictive policies are
/// `AND`-combined on top), following the SQL-standard row-level-security model.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CreatePolicy {
    /// Policy name (unique per table).
    pub name: String,
    /// The table the policy governs.
    pub table: String,
    /// `true` for `AS PERMISSIVE` (the default when `AS` is omitted); `false` for `AS RESTRICTIVE`.
    pub permissive: bool,
    /// The command the policy applies to.
    pub command: PolicyCommand,
    /// Roles the policy applies to; empty means `PUBLIC` (every role).
    pub roles: Vec<String>,
    /// The `USING (expr)` row-visibility predicate as canonical SQL, or `None` if omitted.
    pub using: Option<String>,
    /// The `WITH CHECK (expr)` write predicate as canonical SQL, or `None` if omitted.
    pub check: Option<String>,
}

/// `DROP POLICY [IF EXISTS] name ON table`.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct DropPolicy {
    /// Policy name.
    pub name: String,
    /// The table the policy is attached to.
    pub table: String,
    /// Whether `IF EXISTS` was specified.
    pub if_exists: bool,
}

/// `ALTER POLICY name ON table [TO role[, ...]] [USING (expr)] [WITH CHECK (expr)]`.
///
/// Each optional clause is `None` when omitted, meaning the existing policy keeps that part; a
/// `Some` replaces it. The policy's kind (permissive/restrictive) and command cannot be changed by
/// `ALTER POLICY` (matching the SQL-standard model), so they are not modelled here.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct AlterPolicy {
    /// Policy name (must already exist on `table`).
    pub name: String,
    /// The table the policy is attached to.
    pub table: String,
    /// New `TO role[, ...]` list, or `None` to keep the existing roles. An empty list is `PUBLIC`.
    pub roles: Option<Vec<String>>,
    /// New `USING (expr)` predicate as canonical SQL, or `None` to keep the existing one.
    pub using: Option<String>,
    /// New `WITH CHECK (expr)` predicate as canonical SQL, or `None` to keep the existing one.
    pub check: Option<String>,
}

