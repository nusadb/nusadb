/// When a trigger fires relative to the triggering write.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum TriggerTiming {
    /// `BEFORE` — fire before the row is written (and before constraint checks).
    Before,
    /// `AFTER` — fire after the row has been written.
    After,
}

impl TriggerTiming {
    /// The canonical keyword, used for catalog persistence and diagnostics.
    #[must_use]
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Before => "before",
            Self::After => "after",
        }
    }
}

/// The DML event a trigger fires on.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum TriggerEvent {
    /// `INSERT`.
    Insert,
    /// `UPDATE`.
    Update,
    /// `DELETE`.
    Delete,
}

impl TriggerEvent {
    /// The canonical keyword, used for catalog persistence and diagnostics.
    #[must_use]
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Insert => "insert",
            Self::Update => "update",
            Self::Delete => "delete",
        }
    }

    /// Parse a canonical keyword back into an event (catalog read). Unknown text → `None`.
    #[must_use]
    pub fn parse_keyword(text: &str) -> Option<Self> {
        match text {
            "insert" => Some(Self::Insert),
            "update" => Some(Self::Update),
            "delete" => Some(Self::Delete),
            _ => None,
        }
    }
}

/// Granularity of a trigger: per affected row, or once per statement.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum TriggerForEach {
    /// `FOR EACH ROW` — the action fires once per affected row, with `NEW`/`OLD` bound (the default
    /// when the clause is omitted, since that is the form that can reference the affected row).
    #[default]
    Row,
    /// `FOR EACH STATEMENT` — the action fires once per statement, with no `NEW`/`OLD` binding.
    Statement,
}

impl TriggerForEach {
    /// The canonical keyword, used for catalog persistence and diagnostics.
    #[must_use]
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Row => "row",
            Self::Statement => "statement",
        }
    }
}

/// `CREATE [OR REPLACE] TRIGGER name {BEFORE|AFTER} {INSERT|UPDATE|DELETE}[ OR ...] ON table
/// [FOR EACH {ROW|STATEMENT}] [WHEN (cond)] <triggered-statement>`.
///
/// The triggered action is a single SQL data statement (stored verbatim as text) that may reference
/// the affected row through the `NEW.col` / `OLD.col` pseudo-columns, substituted with the row's
/// values when the trigger fires. `INSTEAD OF` triggers (which require updatable views) are
/// rejected at the parser.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CreateTrigger {
    /// Trigger name (unique per table).
    pub name: String,
    /// Whether `OR REPLACE` was specified — replace a same-named trigger on the table.
    pub or_replace: bool,
    /// When the trigger fires relative to the write.
    pub timing: TriggerTiming,
    /// The events it fires on (one or more, joined by `OR`); never empty.
    pub events: Vec<TriggerEvent>,
    /// The table the trigger is attached to.
    pub table: String,
    /// Per-row or per-statement granularity.
    pub for_each: TriggerForEach,
    /// The `WHEN (cond)` guard as raw SQL (may reference `NEW`/`OLD`), or `None` if omitted.
    pub when: Option<String>,
    /// The triggered action as raw SQL — a single data statement (may reference `NEW`/`OLD`).
    pub action: String,
}

