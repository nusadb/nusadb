/// `CREATE TABLE name (columns...)`.
// Not `Eq`: a table-level `CHECK` constraint carries an `Expr`, which is only `PartialEq`.
#[derive(Debug, Clone, PartialEq)]
pub struct CreateTable {
    /// Explicit schema qualifier to create the table in: `Some(schema)` for `CREATE TABLE
    /// schema.t`, `None` when unqualified (created in the session's current schema).
    pub schema: Option<String>,
    /// Table name.
    pub name: String,
    /// Columns in declaration order; always non-empty.
    pub columns: Vec<ColumnDef>,
    /// Table-level constraints (`PRIMARY KEY`/`UNIQUE`/`FOREIGN KEY`/`CHECK`), in declaration order.
    pub constraints: Vec<TableConstraint>,
    /// Whether `IF NOT EXISTS` was specified.
    pub if_not_exists: bool,
}

/// `CREATE TABLE [IF NOT EXISTS] name AS <select>`.
///
/// The new table's schema is derived from the query's output (name + type per projected column); use
/// column aliases in the `SELECT` to name the output columns. The query's rows are inserted at
/// creation time.
// Not `Eq`: the `Select` body carries `Expr`s, which are only `PartialEq`.
#[derive(Debug, Clone, PartialEq)]
pub struct CreateTableAs {
    /// Table name.
    pub name: String,
    /// The source query — any supported `SELECT`.
    pub query: Box<Select>,
    /// Whether `IF NOT EXISTS` was specified.
    pub if_not_exists: bool,
}

/// One column definition inside a [`CreateTable`].
// Not `Eq`: `default`/`generated` carry an `Expr`, which is only `PartialEq`.
#[derive(Debug, Clone, PartialEq)]
#[allow(
    clippy::struct_excessive_bools,
    reason = "each bool is an independent column attribute (NULL/PK/UNIQUE/SERIAL), not a state flag"
)]
pub struct ColumnDef {
    /// Column name.
    pub name: String,
    /// Column type, mapped onto the catalog's [`ColumnType`] vocabulary.
    pub ty: ColumnType,
    /// A user-defined type name (e.g. an `ENUM` created by `CREATE TYPE`) the parser could not
    /// resolve to a built-in type, carried so the executor can resolve it against the type catalog at
    /// `CREATE TABLE` time (B-ENUM). `ty` holds the placeholder storage type (`TEXT`); `None` for an
    /// ordinary built-in type.
    pub udt_name: Option<String>,
    /// Whether the column accepts `NULL`. `NOT NULL` and `PRIMARY KEY` clear this.
    pub nullable: bool,
    /// Whether the column carries a `PRIMARY KEY` clause.
    pub primary_key: bool,
    /// Whether the column carries a (non-primary) `UNIQUE` clause.
    pub unique: bool,
    /// `DEFAULT <expr>` — value expression applied when the column is omitted.
    /// Boxed to keep `ColumnDef` small (it lives in `Vec<ColumnDef>` throughout the plan).
    pub default: Option<Box<Expr>>,
    /// The `DEFAULT` expression's canonical SQL text, carried alongside [`default`](Self::default) so
    /// the executor can persist it in the column-default catalog and re-parse it per write,
    /// mirroring how a `CHECK` predicate carries `predicate_sql`. `None` when there is no default.
    pub default_sql: Option<String>,
    /// `GENERATED ALWAYS AS (<expr>) [STORED|VIRTUAL]` — a computed column.
    pub generated: Option<GeneratedColumn>,
    /// `true` for a `SERIAL`/`BIGSERIAL`/`SMALLSERIAL` column: an auto-incrementing `INT`
    /// backed by a per-column sequence. Implies `NOT NULL`; an omitted value gets the next sequence
    /// value. Mutually exclusive with an explicit `DEFAULT`.
    pub serial: bool,
    /// `true` only for `GENERATED ALWAYS AS IDENTITY` (a stricter [`serial`](Self::serial)): an
    /// explicit value is rejected at `INSERT` (#9a). `SERIAL` and `GENERATED BY DEFAULT AS IDENTITY`
    /// leave this `false` (they accept an explicit value).
    pub identity_always: bool,
}

/// A `GENERATED ALWAYS AS (<expr>) [STORED|VIRTUAL]` computed column.
#[derive(Debug, Clone, PartialEq)]
pub struct GeneratedColumn {
    /// The generation expression (boxed to keep `ColumnDef` small).
    pub expr: Box<Expr>,
    /// The generation expression's canonical SQL text, captured at parse time so it can be persisted
    /// in the column catalog and re-analyzed against the table per write, as `DEFAULT` does.
    pub sql: String,
    /// `true` for `STORED`, `false` for `VIRTUAL` (or unspecified — virtual is the default).
    pub stored: bool,
}

