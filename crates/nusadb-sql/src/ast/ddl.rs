/// `DROP DATABASE [IF EXISTS] name` — drop every table in the current single database.
///
/// NusaDB is one database per data directory, so this empties the data directory's tables rather
/// than isolating a separate database — but **only when `name` matches the current database**;
/// dropping any other name is a no-op (a non-matching database is left untouched). As a data-loss safety net (NusaDB
/// extension), the plain form first **backs up** every table — its columns *and* rows — to
/// `{name}_{datetime}_{table}` before dropping the originals. The canonical `DROP DATABASE name FORCE`
/// form (or the `FIX DROP DATABASE name` alias) sets [`force`](Self::force) to drop permanently
/// **without** a backup.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct DropDatabase {
    /// Database name.
    pub name: String,
    /// Whether `IF EXISTS` was specified.
    pub if_exists: bool,
    /// `true` for `DROP DATABASE name FORCE` (or the `FIX DROP DATABASE name` alias) — drop
    /// permanently, skipping the safety backup.
    pub force: bool,
}

/// `ALTER DATABASE name ...` — a single-database compatibility no-op.
